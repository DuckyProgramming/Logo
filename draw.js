function draw(){
    graphics.full.background(0)
    graphics.full.noStroke()
    graphics.full.push()
    graphics.full.translate(300,300)
    graphics.full.scale(graphics.full.sin(min(stage.time*4,180))/2)
    graphics.full.rotate(stage.time*2)
    for(a=3;a>=0;a--){
        graphics.full.rotate(7.5)
        for(b=0;b<24;b++){
            graphics.full.rotate(15)
            graphics.full.fill(255,0.5)
            graphics.full.quad(0,0,sqrt(14*(a*0.25+1))*10.55,sqrt(14*(a*0.25+1))*80,0,sqrt(28*(a*0.25+1))*80,sqrt(14*(a*0.25+1))*-10.55,sqrt(14*(a*0.25+1))*80)
        }
    }
    for(a=16;a>=0;a--){
        graphics.full.rotate(7.5)
        for(b=0;b<24;b++){
            graphics.full.rotate(15)
            graphics.full.fill(160+a*5)
            graphics.full.quad(0,0,sqrt(a)*11,sqrt(a)*80,0,sqrt(a+1)*80,sqrt(a)*-11,sqrt(a)*80)
        }
    }
    graphics.full.rotate(-157.5-stage.time*2)
    graphics.full.fill(0)
    graphics.full.triangle(-100,100,-200,100,-100,200)
    graphics.full.beginShape()
    graphics.full.vertex(-100,100)
    graphics.full.vertex(-100,-175)
    graphics.full.vertex(-25,-25)
    graphics.full.vertex(175,-175)
    graphics.full.vertex(25,25)
    graphics.full.vertex(175,100)
    graphics.full.endShape()
    graphics.full.pop()
    image(graphics.full,0,0,600,600)
    stage.time++;
}